/*
 * This program demonstrates variable declaration statements, then
 * outputs the stored values.
 */

public class DeclaringVariablesFinished {

    public static void main(String[] args) {
        int age = 25;
        double height = 71.5;
        String name = "David";

        System.out.println(age);
        System.out.println(height);
        System.out.println(name);

        age = 75;
        System.out.println(age);

        name = "Dave";
    }

}
