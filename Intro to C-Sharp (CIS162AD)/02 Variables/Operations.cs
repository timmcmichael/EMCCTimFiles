/*
 * This program demonstrates casting to calculate an accurate average.
 *
 */

int ann = 15;
int deidre = 17;
int vanessa = 17;
int numFriends = 3;
int totalAges = ann + deidre + vanessa;
double avgAge = totalAges / (double)numFriends;
Console.WriteLine("Average =" + avgAge);

